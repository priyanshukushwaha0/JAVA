public class datatype{
    public static void main(String args[]){
        byte b =8;
        System.out.println(b);
        char ch = 'b';            //all character
        System.out.println(ch);
        boolean var = false;      //true,false 
        System.out.println(var);
        int number = 9;           //simple num. no decimal
        System.out.println(number);
        float price=9.6;           //decimal
        System.out.println(price);
        long pricr = 1;   //if more no. of simple num
        System.out.println(pricr);
        double nums=1.7;  //if more no. of decimals
        System.out.println(nums);
        



    }
}